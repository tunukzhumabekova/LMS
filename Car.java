public class Car {

        private String name;
        private int number;
        private int price;

        public String getName() {
            return name;
        }
        public void setName(String name) {
            this.name = name;
        }



        public int getNumber() {
            return number;
        }
        public int setNumber(int number) {
            this.number = number;
            return number;
        }

        public int getPrice() {
            return price;
        }
        public int setPrice(int speed) {
            this.price = speed;
            return speed;
        }
    }

